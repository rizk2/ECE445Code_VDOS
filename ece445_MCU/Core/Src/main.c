#define USE_HAL_SAI_REGISTER_CALLBACKS 1
#define AUDIO_BUFFER_LENGTH 1024U
#define AUDIO_BUFFER_LENGTH_HALF AUDIO_BUFFER_LENGTH / 2
#define PDM_BUFFER_LENGTH AUDIO_BUFFER_LENGTH * 128 / 8 //Decimation factor of 128 and 8 bits per byte.
#define PDM_BUFFER_LENGTH_HALF PDM_BUFFER_LENGTH / 2
#define RECORD_VOLTAGE 0

volatile uint8_t flag1 = 0;
volatile uint8_t flag2 = 0;
volatile uint32_t missedFrames = 0;
