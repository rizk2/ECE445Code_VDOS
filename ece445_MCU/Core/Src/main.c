{
	if(flag2){
		missedFrames++;
	}
	flag2 = 1;
	numFrames++;
}

uint32_t getBatteryVoltage(ADC_HandleTypeDef* hadc1){
	HAL_ADC_Start(hadc1);
	HAL_ADC_PollForConversion(hadc1, 20);
	uint32_t ADC_VAL = HAL_ADC_GetValue(hadc1);
	/*float batteryVoltage = 0.0014989*(float)ADC_VAL - 0.2286306;*/
	HAL_ADC_Stop(hadc1);
	return ADC_VAL;
}

